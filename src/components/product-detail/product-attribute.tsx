import {
  Accordion,
  AccordionSummary,
  Typography,
  AccordionDetails,
} from "@mui/material";
import React from "react";
import ExpandMoreIcon from "@mui/icons-material/ExpandMore";
import _ from "lodash";

export default function ProductAttribute(props: {
  sizes: any;
  other: any;
  inc_toppings: any;
  extra_toppings: any;
}) {
  let otherAttrGroup: any;
  if (props.other) {
    otherAttrGroup = attributeGroupObj[_.keys(props.other)[0]];
  }
  return (
    <div>
      {props.sizes ? (
        <Accordion>
          <AccordionSummary expandIcon={<ExpandMoreIcon />}>
            <Typography>Size</Typography>
          </AccordionSummary>
          <AccordionDetails>
            <Typography>
              {_.values(props.sizes)?.map((sizeAttr) => {
                return atributeObj[sizeAttr.id]?.name + sizeAttr.price;
              })}
            </Typography>
          </AccordionDetails>
        </Accordion>
      ) : (
        ""
      )}
      {props.inc_toppings?.length ? (
        <Accordion>
          <AccordionSummary expandIcon={<ExpandMoreIcon />}>
            <Typography> Ingredients, Un-tick to remove. </Typography>
          </AccordionSummary>
          <AccordionDetails>
            <Typography>
              {props.inc_toppings?.map((toppingAttrId: string) => {
                return atributeObj[toppingAttrId]?.name;
              })}
            </Typography>
          </AccordionDetails>
        </Accordion>
      ) : (
        ""
      )}
      {props.extra_toppings ? (
        <Accordion>
          <AccordionSummary expandIcon={<ExpandMoreIcon />}>
            <Typography>Extra</Typography>
          </AccordionSummary>
          <AccordionDetails>
            <Typography>
              {_.values(props.extra_toppings)?.map((extraAttr) => {
                return atributeObj[extraAttr.id]?.name + extraAttr.price;
              })}
            </Typography>
          </AccordionDetails>
        </Accordion>
      ) : (
        ""
      )}

      {otherAttrGroup ? (
        <Accordion>
          <AccordionSummary expandIcon={<ExpandMoreIcon />}>
            <Typography>{otherAttrGroup?.option_label || "Other"}</Typography>
          </AccordionSummary>
          <AccordionDetails>
            <Typography>
              {_.values(_.values(props.other)[0])?.map((otherAttr) => {
                return (
                  atributeObj[otherAttr.attribute_id]?.name +
                  atributeObj[otherAttr.attribute_id]?.price
                );
              })}
            </Typography>
          </AccordionDetails>
        </Accordion>
      ) : (
        ""
      )}
    </div>
  );
}

